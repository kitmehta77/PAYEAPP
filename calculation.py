def add(num1, num2):
    return num1 + num2


def subtract(num1, num2):
    return num1 - num2


def multiply(num1, num2):
    return num1 * num2


def divide(num1, num2):
    if num2 == 0:
        return "number 2 cannot be 0"
    else:
        return num1 / num2


def power(num1, num2):
    return num1 ** num2
